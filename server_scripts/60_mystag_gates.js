// priority: 15
// requires: mysticalagriculture
// ============================================================================
//  Mystical Agriculture — защита чужих прогрессий.
//
//  ПРОБЛЕМА. MystAg превращает эссенцию в ЛЮБОЙ ресурс, в том числе в те
//  предметы, которые в своих модах являются ГЕЙТОМ, а не расходником.
//  Проверено по джарникам, вот самое злое:
//
//    ae2:*_press          8 эссенций церта -> инскрайбер-пресс.
//                         В AE2 прессы падают ТОЛЬКО с метеоритов; церт —
//                         низкотировая культура. Весь ранний AE2 обходится
//                         грядкой на старте.
//    ae2:sky_stone_block  9 эссенций -> 16 блоков небесного камня (метеорит).
//    powah:crystal_*      9 эссенций -> кристалл Powah. Это ровно тот гейт,
//                         который мы строим в 50_powah_gates.js — грядка его
//                         отменяет. Insanium-тир даёт crystal_nitro СРАЗУ x12.
//    powah:uraninite      топливо реакторов. Фармится -> нерф чисел в
//                         powah.json5 становится бессмысленным.
//    draconium / awakened_draconium_nugget x3 -> вся прогрессия Draconic.
//    nether_star_shard    -> незер-звезда (база nitro-кристалла и маяков).
//
//  РЕШЕНИЕ. Хирургия, а не кастрация мода. Убираем ТОЛЬКО конвертации
//  «эссенция -> предмет, который является гейтом другого мода». Сами культуры,
//  семена и вся лестница эссенций остаются нетронутыми: MystAg сохраняет свою
//  роль — массовая ферма расходников.
//
//  ПОЧЕМУ УДАЛЕНИЕ, А НЕ УДОРОЖАНИЕ. Обычно мы гейтим, а не режем. Но здесь
//  любой «гейт» на конвертацию оставил бы параллельный дешёвый путь в обход
//  метеоритов/сплавов. Цель — вернуть предмету его законный источник, поэтому
//  конвертация убирается целиком.
//
//  ПОЧЕМУ НЕ ТРОГАЕМ ЛЕСТНИЦУ ЭССЕНЦИЙ. Inferium->...->Supremium это массовый
//  крафт 4:1 (супремиум = 256 инфериума). Любой расходный тех-компонент на
//  этих рецептах превратился бы в чудовищный гринд — ровно то, чего мы избегаем.
//
//  ОСТАЁТСЯ ФАРМИТЬСЯ (это нормально, в этом смысл мода):
//    ванильная руда и камни, алмазы/изумруды, еда, красители, дерево,
//    дроп мобов, certus_quartz и fluix (AE2 и сам растит церт кристаллами),
//    осмий/фторит Mekanism, сплавы Ender IO, Refined Storage.
//
//  СОПУТСТВУЮЩАЯ ПРАВКА КОНФИГА -> config/mysticalagriculture-common.toml:
//    requiresEffectiveFarmland = false -> true
//  Культуры теперь требуют профильную эссенц-землю своего тира. Это оживляет
//  мёртвый контент мода и даёт расширению фермы честную цену по тирам,
//  не превращая её в гринд.
//
//  Удаляем по паре мод+выход, а не по ID пути: пути рецептов между версиями
//  переезжают (на этом уже обожглись с powah:crafting/...), а мод и выход — нет.
// ============================================================================

ServerEvents.recipes(event => {

  // Снести конвертацию «эссенция -> предмет» из указанного мода-источника
  const cut = (sourceMod, output) => {
    event.remove({ mod: sourceMod, output: output })
  }

  // --- Гейт AE2: прессы падают только с метеоритов --------------------------
  ;[
    'ae2:calculation_processor_press',
    'ae2:engineering_processor_press',
    'ae2:logic_processor_press',
    'ae2:silicon_press',
    'ae2:sky_stone_block'
  ].forEach(id => cut('mysticalagriculture', id))

  // --- Гейт Powah: кристаллы идут через сплавы Mekanism (50_powah_gates.js) --
  ;[
    'powah:crystal_blazing',
    'powah:crystal_niotic',
    'powah:crystal_spirited',
    'powah:uraninite'
  ].forEach(id => cut('mysticalagriculture', id))

  // --- Гейт Draconic: драконий добывается, а не выращивается -----------------
  cut('mysticalagriculture', 'draconicevolution:draconium_ingot')

  // ==========================================================================
  //  Agradditions — тир 6 (insanium). Здесь фармится эндгейм всего пака.
  // ==========================================================================
  if (Platform.isLoaded('mysticalagradditions')) {
    ;[
      'powah:crystal_nitro',                             // верх Powah, шёл x12
      'draconicevolution:awakened_draconium_nugget',     // эндгейм Draconic, x3
      'mysticalagradditions:nether_star_shard'           // незер-звезда
    ].forEach(id => cut('mysticalagradditions', id))
  }
})
