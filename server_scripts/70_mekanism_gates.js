// ============================================================================
//  Mekanism — точечная правка двух выбросов.
//
//  СНАЧАЛА ГЛАВНОЕ: Mekanism внутри сбалансирован ХОРОШО, и это редкость.
//  Проверено по джарникам — обогащение руды честно тирится по лестнице сплавов:
//      2x Enrichment  <- basic alloy   + basic circuit
//      3x Purification<- infused alloy + advanced circuit
//      4x Injection   <- reinforced    + elite circuit
//  Сплавы: медь+редстоун -> +алмаз -> +очищенный обсидиан. Схемы идут следом.
//  Синтез гейтится полонием от деления. Всё это НЕ ТРОГАЕМ: мод честно
//  выстроен и работает спиной прогрессии пака (на его сплавах висит гейт
//  Powah из 50_powah_gates.js).
//
//  Выбиваются ровно два предмета.
//
//  1) КАРТОННАЯ КОРОБКА — 4 древесной пыли, и всё. При этом она поднимает
//     ЛЮБОЙ блок ВМЕСТЕ С СОДЕРЖИМЫМ, включая машины чужих модов. Аномалия:
//     весь остальной мод требует стальной корпус и схемы, а универсальный
//     переносчик машин стоит четыре палки. Классический предмет из бан-листов.
//     Делаем её осознанным средним крафтом, а не находкой первого дня.
//     Выход 4 шт — чтобы не превратить перенос базы в гринд.
//
//  2) РЕАКТОР ДЕЛЕНИЯ — гейтится elite-схемами (середина мода), а даёт
//     колоссальную мощность и может расплавиться, снеся базу. Это ровно то,
//     что режут на серверах.
//     Гейтим УПРАВЛЯЮЩИЙ СТЕРЖЕНЬ: его нужно 1 на реактор, поэтому цена
//     платится один раз и гринда не создаёт. Корпуса (4 за крафт) и топливные
//     сборки нужны пачками — их не трогаем принципиально.
//     В стержень идёт драконий: после правки MystAg (60_mystag_gates.js) он
//     больше не фармится грядкой, то есть требует реальной добычи.
//     История: реакцию держит стержень из экзотического сплава.
//
//  Удаляем по паре тип+выход — пути ID между версиями переезжают.
// ============================================================================

ServerEvents.recipes(event => {

  // --- 1. Картонная коробка --------------------------------------------------
  event.remove({ type: 'minecraft:crafting_shaped', output: 'mekanism:cardboard_box' })
  event.shaped(Item.of('mekanism:cardboard_box', 4), [
    'SWS',
    'WCW',
    'SWS'
  ], {
    S: '#c:ingots/steel',
    W: '#c:dusts/wood',
    C: 'mekanism:basic_control_circuit'
  }).id('kubejs:mekanism/cardboard_box')

  // --- 2. Управляющий стержень реактора деления ------------------------------
  if (Platform.isLoaded('mekanismgenerators') && Platform.isLoaded('draconicevolution')) {
    event.remove({
      type: 'minecraft:crafting_shaped',
      output: 'mekanismgenerators:control_rod_assembly'
    })
    event.shaped('mekanismgenerators:control_rod_assembly', [
      'IDI',
      'SCS',
      'SIS'
    ], {
      I: '#c:ingots/lead',
      D: 'draconicevolution:draconium_ingot',
      S: '#c:ingots/steel',
      C: '#c:circuits/elite'
    }).id('kubejs:mekanism/control_rod_assembly')
  }
})
