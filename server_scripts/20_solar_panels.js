// priority: 50
// requires: solarflux
// ============================================================================
//  Панели Solar Flux Reborn T1 - T8.
//
//  Единая схема:  CCC   C = фотоэлемент SFR нужного тира
//                 PXP   P = предыдущая панель
//                 PYP   X, Y = компоненты из 10_solar_components.js
//
//  Генерация по дефолтному конфигу SFR (config/solarflux/):
//  T1=1  T2=8  T3=32  T4=128  T5=512  T6=2048  T7=8192  T8=32768 FE/t
// ============================================================================

ServerEvents.recipes(event => {
  const S = global.SOLAR
  if (!S) {
    console.error('[SolarFlux] global.SOLAR отсутствует. Проверь, что startup_scripts/00_solar_globals.js на месте, и перезапусти сервер — /reload startup-скрипты не перечитывает.')
    return
  }
  const T = S.tag
  const P = t => S.panels[t - 1]     // панель тира t
  const C = t => S.cells[t - 1]      // фотоэлемент тира t

  // Удаление рецептов SFR через KubeJS НЕ РАБОТАЕТ: их регистрирует HammerLib
  // уже после того, как KubeJS отработал. Ванильные крафты панелей отключаются
  // в config/solarflux/recipes.cfg (ключи solarflux:solar_panel_1..8 = false).
  // Строка ниже безвредна, но ничего не делает.
  // --- Сносим стандартные рецепты -------------------------------------------
  for (let i = 1; i <= 8; i++) event.remove({ output: P(i) })

  // --- Хелпер ----------------------------------------------------------------
  const panel = (tier, cell, x, y, suffix) => {
    return event.shaped(P(tier), [
      'CCC',
      'PXP',
      'PYP'
    ], { C: C(cell), P: P(tier - 1), X: x, Y: y })
      .id(`kubejs:solar/sp_${tier}${suffix ? '_' + suffix : ''}`)
  }

  // ==========================================================================
  //  T1 - 1 FE/t | без машин, стартовая панель
  // ==========================================================================
  event.shaped(P(1), [
    'GGG',
    'LEL',
    'IRI'
  ], {
    G: T.glassPane,
    L: S.lens,
    E: C(1),
    I: T.iron,
    R: T.redstone
  }).id('kubejs:solar/sp_1')

  // ==========================================================================
  //  T2 - 8 FE/t | гейт: сталь + линзы
  // ==========================================================================
  panel(2, 1, S.lens, T.steel)

  // ==========================================================================
  //  T3 - 32 FE/t | гейт: Powah + Mekanism (через Photon Capacitor)
  // ==========================================================================
  panel(3, 2, S.capacitor, S.lens)

  // ==========================================================================
  //  T4 - 128 FE/t | тот же гейт, вдвое дороже
  // ==========================================================================
  panel(4, 2, S.capacitor, S.capacitor)

  // ==========================================================================
  //  T5 - 512 FE/t | гейт: AE2 + IF + MI (через Quantum Solar Matrix)
  // ==========================================================================
  panel(5, 3, S.matrix, S.capacitor)

  // ==========================================================================
  //  T6 - 2048 FE/t | тот же гейт, вдвое дороже
  // ==========================================================================
  panel(6, 3, S.matrix, S.matrix)

  // ==========================================================================
  //  T7 - 8192 FE/t | гейт: МАГИЯ (Ars Nouveau + Occultism)
  //  Шаблон ломается: панелей нужно 2, а не 4 — иначе экспонента 4^n
  //  делает верхние тиры физически недостижимыми.
  // ==========================================================================
  event.shaped(P(7), [
    'CAC',
    'PMP',
    'CAC'
  ], {
    C: C(4),
    A: S.arcane,
    P: P(6),
    M: S.matrix
  }).id('kubejs:solar/sp_7')

  // ==========================================================================
  //  T8 - 32768 FE/t | гейт: Draconic Evolution
  // ==========================================================================
  event.shaped(P(8), [
    'CDC',
    'PAP',
    'CDC'
  ], {
    C: C(4),
    D: S.draconic,
    P: P(7),
    A: S.arcane
  }).id('kubejs:solar/sp_8')

  // ==========================================================================
  //  Разбор панели обратно на панели предыдущего тира.
  //  Возвращает половину вложенного: T2-T6 собираются из 4 панелей -> дают 2,
  //  T7-T8 собираются из 2 -> дают 1. Компоненты при разборе теряются,
  //  так что цикл крафт/разбор всегда убыточен.
  // ==========================================================================
  for (let i = 2; i <= 8; i++) {
    event.shapeless(Item.of(P(i - 1), i <= 6 ? 2 : 1), [P(i)])
      .id(`kubejs:solar/sp_${i}_dismantle`)
  }
})
