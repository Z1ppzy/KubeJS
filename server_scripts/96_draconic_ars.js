// ============================================================================
//  Мост Draconic Evolution <-> Ars Nouveau.
//
//  ЗАЧЕМ. Это два эндгейма пака, и до сих пор они шли параллельно: техника
//  не нуждалась в магии, магия в технике. Единственная связь была с нашей
//  стороны — арканное солнечное ядро (панель VII) требует материалы Ars.
//
//  ГДЕ СТЫК. У DE есть ровно одна точка, где мод перестаёт быть «просто
//  технологией»: ПРОБУЖДЕНИЕ дракония. Рецепт буквально ритуальный —
//  4 блока дракония как катализатор, шесть ядер по инжекторам и СЕРДЦЕ
//  ДРАКОНА в центре, 50 млн FE. Это не сборка машины, это обряд.
//
//  А у Ars есть свой трофей того же ранга — ДАНЬ ДИКИХ с Дикой Химеры,
//  их главного босса. Симметрия сама напрашивается:
//      сердце дракона  = трофей Края, мощь технической ветки
//      дань Диких      = трофей Химеры, мощь магической ветки
//  Пробуждение требует обоих. История: мёртвый металл оживает только там,
//  где сходятся обе силы.
//
//  ЧТО ЭТО ДАЁТ МЕХАНИЧЕСКИ. Пробуждённый драконий — вход во ВЕСЬ верхний
//  DE: awakened_core -> chaotic_core, инструменты и броня третьего тира,
//  реактор, и наше драконическое солнечное ядро (панель VIII).
//  Теперь ничего из этого нельзя получить, не победив босса Ars.
//  Два эндгейма сцеплены одной правкой.
//
//  ПОЧЕМУ ГЕЙТ ЧЕСТНЫЙ:
//   - Химера ПОВТОРЯЕМА: ritual_wilden_summon крафтится из архвуда,
//     трёх дропов Диких и лазуритового блока. Тупика не создаём.
//   - Объём низкий: один рецепт даёт 4 блока (36 слитков), пробуждают редко.
//   - Обхода нет: awakened_draconium_dust в сборке не производит НИКТО
//     (проверено по всем джарникам), значит фьюжен — единственный источник.
//
//  ТЕХНИКА. Катализатор использует нестандартный тип draconicevolution:stack
//  (4 блока одним стеком) — переносим его как есть. Число ингредиентов
//  сохраняем: не добавляем восьмой, а меняем одно из шести ядер на дань.
// ============================================================================

ServerEvents.recipes(event => {

  event.remove({
    type: 'draconicevolution:fusion_crafting',
    output: 'draconicevolution:awakened_draconium_block'
  })

  const core = { consume: true, ingredient: { item: 'draconicevolution:draconium_core' } }

  event.custom({
    type: 'draconicevolution:fusion_crafting',
    catalyst: {
      type: 'draconicevolution:stack',
      count: 4,
      items: 'draconicevolution:draconium_block'
    },
    ingredients: [
      core,
      core,
      core,
      { consume: true, ingredient: { item: 'draconicevolution:dragon_heart' } },
      core,
      core,
      // было шестое ядро — теперь трофей магической ветки
      { consume: true, ingredient: { item: 'ars_nouveau:wilden_tribute' } }
    ],
    result: { count: 4, id: 'draconicevolution:awakened_draconium_block' },
    techLevel: 'wyvern',
    totalEnergy: 50000000
  }).id('kubejs:bridge/awakened_draconium_block')
})
